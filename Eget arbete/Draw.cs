        public static void Dungeon () {
            for (int j = 0; j < Program.imgHeight; j ++) {
                for (int i = 0; i < Program.imgWidth; i ++) {
                    Program.image[i,j] = dungeon[i + j * (Program.imgWidth)];
                }
            }
        }
        public static bool DungeonAt(int i, int j) {
            switch (dungeon[i + j * (Program.imgWidth)]) {
                case ' ':
                return true;
                
                case '=':
                if (! Program.flag[0]) {
                    Program.place = "lookPool";
                }
                return true;

                case 'ö':
                Program.place = "slime";
                return false;
            }
            return false;
